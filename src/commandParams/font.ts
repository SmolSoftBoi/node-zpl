/**
 * Field orientation.
 */
export const enum FieldOrientation {

    /**
     * Normal.
     */
    NORMAL = 'N',

    /**
     * Rotated 90 degrees (clockwise).
     */
    DEGREES_90 = 'R',

    /**
     * Inverted 180 degrees.
     */
    DEGREES_180 = 'I',

    /**
     * Read from bottom up, 270 degrees.
     */
    DEGREES_270 = 'B'
}